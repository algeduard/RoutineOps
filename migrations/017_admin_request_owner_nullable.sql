-- 017: владелец заявки на админ-доступ становится необязательным.
--
-- Зачем: MDM-пользователи — это ИТ-операторы/администраторы, а не сотрудники,
-- поэтому понятие «владелец устройства» не имеет смысла как предусловие. Раньше
-- запрос админ-прав с трея блокировался, если у устройства не назначен владелец.
-- Теперь requested_by может быть NULL — заявка оформляется и без владельца.
ALTER TABLE admin_access_requests ALTER COLUMN requested_by DROP NOT NULL;
