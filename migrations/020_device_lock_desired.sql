-- Желаемое (авторитетное) состояние блокировки устройства для реконсиляции.
--
-- Канал лока был push-only: команда блокировки приезжала агенту один раз таском по
-- Connect-стриму. Локальный unlock мог обнулить lock.json, не доехав до сервера, и
-- после ребута агент читал локальный `unlocked` → лок терялся (сервер при этом висел
-- в `locked`). Чтобы агент мог поллить желаемое состояние (FetchLockStatus) и
-- пере-заблокироваться, серверу нужно хранить не только флаг, но и bcrypt-хеш пароля
-- и причину — их агент кладёт в lock.json, чтобы локально верифицировать разблокировку.
--
-- lock_status уже существует и трактуется как ЖЕЛАЕМОЕ состояние (выставляется
-- эндпоинтами lock/unlock). Добавляем сопутствующие поля.
ALTER TABLE devices ADD COLUMN IF NOT EXISTS lock_hash   TEXT NOT NULL DEFAULT '';
ALTER TABLE devices ADD COLUMN IF NOT EXISTS lock_reason TEXT NOT NULL DEFAULT '';
