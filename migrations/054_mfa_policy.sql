-- 054: MFA enforce-by-policy — организационная политика «требовать 2FA». БАЗОВАЯ фича
-- (НЕ за лицензией), как и сам TOTP MFA (044). Даёт it_admin возможность ОБЯЗАТЬ включение
-- MFA для роли (только it_admin | все), с принуждением на уровне API-гейта.
--
-- БЕЗОПАСНОСТЬ НАКАТА: политика по умолчанию '' (ВЫКЛ) → ни один существующий логин/сессия
-- не блокируется, поведение идентично докатному (как MFA-опциональность до 044). Включение
-- политики — осознанное действие админа уже после наката.
--
-- Накат: scripts/migrate.sh применяет файл РОВНО ОДИН РАЗ в --single-transaction, поэтому
-- здесь плоские CREATE TABLE / INSERT БЕЗ IF NOT EXISTS — как в 001..050.

-- ── Организационные настройки (singleton) ────────────────────────────────────
-- Ровно одна строка на инсталляцию: id — BOOLEAN PK с CHECK(id) (может быть только true),
-- поэтому вторую строку вставить нельзя (PK-уникальность + единственное допустимое значение).
-- mfa_required_role — политика принуждения MFA:
--   ''         = выключено (дефолт; поведение как до фичи — MFA добровольна);
--   'it_admin' = MFA обязательна для роли it_admin;
--   'all'      = MFA обязательна для всех человеческих ролей.
-- Гейт (internal/server/api/mfa_policy.go) для юзера под политикой без totp_enabled блокирует
-- authed-действия (403 mfa_required), КРОМЕ allowlist'а эндпоинтов включения MFA — анти-локаут.
CREATE TABLE org_settings (
  id                BOOLEAN PRIMARY KEY DEFAULT true CHECK (id),
  mfa_required_role TEXT NOT NULL DEFAULT '',
  updated_at        TIMESTAMPTZ NOT NULL DEFAULT now()
);

