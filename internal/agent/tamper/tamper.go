// Package tamper реализует защиту агента от удаления (tamper-protection),
// устойчивую к безопасному режиму Windows.
//
// Модель (детали — в tamper_windows.go):
//
//   - На install агент взводит защиту: пишет флаги в реестр
//     (HKLM\SOFTWARE\RoutineOps\Agent: TamperProtection, SafeBootGuard = 1) и
//     регистрирует службу в SafeBoot\Minimal|Network, чтобы она запускалась и в
//     безопасном режиме (тот же приём, что у AV/EDR). Пока служба запущена, её exe
//     залочен и не удаляется ни в обычном, ни в безопасном режиме.
//
//   - В ОБЫЧНОМ режиме при взведённых флагах работает enforce-цикл: восстанавливает
//     SafeBoot-ключи и возвращает флаги в 1, если их обнулили «на ходу». Значит,
//     сбросить защиту в обычном режиме нельзя — агент тут же перевзводит её.
//
//   - В БЕЗОПАСНОМ режиме enforce НЕ работает: служба запущена (exe залочен), но
//     флаги не трогает. Только здесь админ может durably выставить их в 0.
//
//   - После reboot в обычный режим агент читает флаги=0 на старте → пассивный режим
//     (ничего не перевзводит, не мешает удалению). Дальше uninstall.bat от админа
//     снимает SafeBoot-ключи, удаляет службу и файлы.
//
// Итог: снять агента может только админ по процедуре «безопасный режим → флаги в 0
// → reboot → батник». Обычный пользователь не может (правка HKLM и вход в
// безопасный режим требуют прав администратора).
//
// macOS (tamper_darwin.go) — упрощённый вариант: Arm вешает immutable-флаг schg на
// /usr/local/bin/RoutineOps-agent и оба Launch-plist'а, ядро не даёт их удалить даже root'у,
// поэтому сторож не нужен (Enforce — no-op). Disarm снимает флаг (chflags noschg) и
// является ОБЯЗАТЕЛЬНЫМ шагом перед service.Uninstall, иначе os.Remove(plist) вернёт
// EPERM. Cleanup — no-op: снимать нечего.
//
// Linux (tamper_other.go) — защиты нет: Arm/Cleanup/Enforce no-op, Disarm возвращает
// ErrUnsupported.
